package com.fi.crm.campaigns.common.dto;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name = "Province")
public class ProvinceDTO implements BaseDTO{
	/**
	 * 
	 */
	private static final long serialVersionUID = 3541834751464620800L;
	private String id;
	private String name;
	private String countryId;

	public ProvinceDTO(String id) {
		super();
		this.id = id;
	}

	public ProvinceDTO() {
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCountryId() {
		return countryId;
	}

	public void setCountryId(String countryId) {
		this.countryId = countryId;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ProvinceDTO other = (ProvinceDTO) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}
	
}
